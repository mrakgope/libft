int	ft_atoi(const char *str)
{
	int i;
	int syn;
	int rslt;

	i = 0;
	syn = 1;
	rslt = 0;
	if (str[i] == '\n' || str[i] == '\v' || str[i] == '\t' || str[i] == ' ')
		str++;
	if (str[i] == '-')
	{
		syn = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		rslt = rslt * 10 + str[i] - '0';
		i++;
	}
	return (syn * rslt);
}
