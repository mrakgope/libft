#include "libft.h"

int	ft_strcmp(const char *s, const char *t)
{
	unsigned int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != t[i])
			break ;
		i++;
	}
	return (s[i] - t[i]);
}
