#include "libft.h"

int	ft_strcmp(const char *s, const char *t)
{
	unsigned int i;

	i = 0;
	while (*s != '\0')
	{
		if (*s != *t)
			break ;
		s++;
		t++;
	}
	return (*s - *t);
}
