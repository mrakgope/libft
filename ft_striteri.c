#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned	i;

	i = 0;
	if (s[i] != '\0' && f[i] != '\0')
	{
		while (s[i] != '\0')
		{
			(*f)(i, s);
			i++;
			s++;
		}
	}
}
