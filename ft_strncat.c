#include "libft.h"

char	*ft_strncat(char *s, const char *t, int c)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (s[a] != '\0')
		a++;
	while (c > b && t[b] != '\0')
	{
		s[a] = t[b];
		b++;
		a++;
	}
	s[a] = '\0';
	return (s);
}
