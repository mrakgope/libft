#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (s1[a] != '\0')
		a++;
	while (n-- && s2[b] != '\0')
	{
		s1[a + b] = s2[b];
		b++;
	}
	s1[a + b] = '\0';
	return (s1);
}
